static char *read_all(const char *path, size_t *len_out) {
    FILE *fp = fopen(path, "rb");
    long n;
    char *buf;
    if (!fp) return NULL;
    if (fseek(fp, 0, SEEK_END) != 0) { fclose(fp); return NULL; }
    n = ftell(fp);
    if (n < 0) { fclose(fp); return NULL; }
    if (fseek(fp, 0, SEEK_SET) != 0) { fclose(fp); return NULL; }
    buf = (char *)malloc((size_t)n + 1);
    if (!buf) { fclose(fp); return NULL; }
    if (n > 0 && fread(buf, 1, (size_t)n, fp) != (size_t)n) { free(buf); fclose(fp); return NULL; }
    fclose(fp);
    buf[n] = '\0';
    if (len_out) *len_out = (size_t)n;
    return buf;
}

static void print_escaped(const char *s, size_t n) {
    size_t i;
    putchar('"');
    for (i = 0; i < n; i++) {
        unsigned char c = (unsigned char)s[i];
        switch (c) {
        case '\\': fputs("\\\\", stdout); break;
        case '"': fputs("\\\"", stdout); break;
        case '\n': fputs("\\n", stdout); break;
        case '\r': fputs("\\r", stdout); break;
        case '\t': fputs("\\t", stdout); break;
        default:
            if (c < 32 || c > 126) printf("\\x%02X", c);
            else putchar((int)c);
        }
    }
    putchar('"');
}

int main(int argc, char **argv) {
    char *input;
    size_t len = 0;
    size_t pos = 0;
    size_t line = 1, col = 1;
    if (argc < 2) {
        fprintf(stderr, "Usage: %s <input.txt>\n", argv[0]);
        return 1;
    }
    input = read_all(argv[1], &len);
    if (!input) {
        fprintf(stderr, "cannot read input file: %s\n", argv[1]);
        return 1;
    }
    while (pos < len) {
        int state = 0;
        int last_tok = -1;
        size_t i = pos;
        size_t scan_line = line, scan_col = col;
        size_t last_pos = pos;
        size_t last_line = line, last_col = col;
        while (i < len) {
            unsigned char ch = (unsigned char)input[i];
            int next = DFA_TRANS[state][ch];
            if (next < 0) break;
            state = next;
            i++;
            if (ch == '\n') { scan_line++; scan_col = 1; }
            else { scan_col++; }
            if (DFA_ACCEPT[state] >= 0) {
                last_tok = DFA_ACCEPT[state];
                last_pos = i;
                last_line = scan_line;
                last_col = scan_col;
            }
        }
        if (last_tok >= 0) {
            if (!TOKEN_SKIP[last_tok]) {
                printf("TOKEN %s ", TOKEN_NAMES[last_tok]);
                print_escaped(input + pos, last_pos - pos);
                printf(" (line %zu, col %zu)\n", line, col);
            }
            pos = last_pos;
            line = last_line;
            col = last_col;
        } else {
            printf("LEXICAL_ERROR line %zu col %zu: ", line, col);
            print_escaped(input + pos, 1);
            printf("\n");
            if ((unsigned char)input[pos] == '\n') {
                line++;
                col = 1;
            } else {
                col++;
            }
            pos++;
        }
    }
    printf("TOKEN %s \"<EOF>\" (line %zu, col %zu)\n", "EOF", line, col);
    free(input);
    return 0;
}

